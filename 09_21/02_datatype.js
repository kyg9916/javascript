// 문자열 (글자)
let name = "홍길동";
console.log("이름:", name);
console.log("name의 타입:", typeof name);

// 숫자 (정수와 소수)
let age = 20;
let height = 172.5;
console.log("나이:", age);
console.log("age의 타입:", typeof age);
console.log("키:", height);
console.log("height의 타입:", typeof height);

// 불린 (참/거짓)
let isStudent = true;
console.log("학생인가요?:", isStudent);
console.log("isStudent의 타입:", typeof isStudent);

// null (의도적으로 비어 있는 값)
let job = null;
console.log("직업:", job);
console.log("job의 타입:", typeof job); // 결과는 'object' → 자바스크립트의 오래된 버그

// undefined (아직 값이 지정되지 않음)
let hobby;
console.log("취미:", hobby);
console.log("hobby의 타입:", typeof hobby);

