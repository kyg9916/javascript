// 1. var: 옛날 방식 (지금은 거의 안 씀)
var nickname = "탈모빔";
console.log("var로 선언한 nickname:", nickname);

// 2. let: 바꿀 수 있는 변수
let age = 18;
console.log("let으로 선언한 age:", age);
age = 16; // 값 변경 가능
console.log("age를 16으로 바꿈:", age);

// 3. const: 바꿀 수 없는 상수
const name = "천진반";
console.log("const로 선언한 name:", name);

// name = "정국"; // 오류! const는 값을 바꿀 수 없음
